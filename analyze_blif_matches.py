# Ordered list of (prefix, family) pairs.
# The first matching prefix wins, so longer prefixes should come first.
_FAMILY_PREFIXES = [
    ("xor_chain",  "xor_chain"),
    ("mux_tree",   "mux_tree"),
    ("adder",      "adder"),
    ("multiplier", "multiplier"),
    ("random",     "random"),
    ("majority",   "toy"),
    ("mux",        "toy"),
    ("toy",        "toy"),
]


def infer_benchmark_family(benchmark_name: str) -> str:
    """
    Infer a family label from the benchmark file/directory name.

    Used to group results in summary tables and ablation studies.
    Falls back to 'unknown' if no prefix matches.
    """
    bn = benchmark_name.lower()
    for prefix, family in _FAMILY_PREFIXES:
        if bn.startswith(prefix):
            return family
    return "unknown"


# Mapping from optimization name to a coarse effort group.
# 'none' = just strash (no optimisation), increasing through very_high.
_OPT_GROUPS = {
    "original":    "none",
    "balance":     "low",
    "rewrite":     "medium",
    "refactor":    "medium",
    "rewrite_z":   "medium",
    "refactor_z":  "medium",
    "resub":       "high",
    "resyn":       "high",
    "resyn2":      "very_high",
    "resyn2_like": "very_high",
    "dc2":         "very_high",
    "compress2rs": "very_high",
}


def infer_optimization_group(optimization_name: str) -> str:
    """Return the effort group label for a given optimization name."""
    return _OPT_GROUPS.get(optimization_name, "unknown")


# -----------------------------------------------------------------------------
# Matching metrics
# -----------------------------------------------------------------------------


def jaccard(a, b):
    """Jaccard overlap between two supports."""

    if not a and not b:
        return 1.0
    if not a or not b:
        return 0.0
    return len(a & b) / len(a | b)


def bit_similarity(sig_a, sig_b, total_patterns):
    """
    Similarity between two simulated Boolean signatures.

    1.0 means they agree on all simulated patterns.
    0.5 means random-like agreement.
    0.0 means complete disagreement.
    """

    diff = sig_a ^ sig_b
    mismatches = diff.bit_count()
    return 1.0 - (mismatches / total_patterns)


def depth_similarity(depth_a, depth_b):
    """Small helper so nodes at similar depths get a slightly better score."""

    diff = abs(depth_a - depth_b)
    return 1.0 / (1.0 + diff)


def combined_candidate_score(sim_score, support_score, depth_score):
    """
    Weighted combination of simulation similarity, support overlap, and depth proximity.

    Weights chosen empirically for Experiment 2:
      0.55 simulation  — direct Boolean agreement is the strongest signal
      0.35 support     — shared input cone is the best structural backup when
                         simulation is inconclusive (e.g. under random patterns)
      0.10 depth       — depth alone is a weak signal but breaks ties naturally

    This is intentionally a simple, transparent baseline, not the final result.
    """

    return 0.55 * sim_score + 0.35 * support_score + 0.10 * depth_score


def compare_networks(original, optimized):
    """
    Compute summary metrics for original vs optimized network.

    New research metrics added (in addition to the original set):

    exact_match_rate
        Fraction of optimized internal nodes that share a simulation signature
        with at least one original node.  A value of 1.0 means every optimized
        node is bit-for-bit equivalent to some original node (i.e. the optimizer
        only deleted / re-used nodes, never introduced genuinely new logic).

    node_reduction_rate / level_reduction_rate
        Signed relative change.  Positive means the network shrank.

    avg_best_simulation_similarity
        For each optimized node, find the best (max) bit-similarity to any
        original node; average those bests.  More sensitive than exact_match_rate
        because near-matches score > 0.

    avg_best_combined_score
        Same per-node-best approach, but using the combined_candidate_score
        (0.55 sim + 0.35 support + 0.10 depth) so depth and support contribute.

    support_overlap_min / _median / _max
        Distribution of the per-optimized-node best Jaccard support overlap.
        Useful for spotting outlier nodes whose support changed drastically.
    """

    original_sigs = original["signatures"]
    optimized_sigs = optimized["signatures"]
    orig_nodes = original["num_internal_nodes"]
    opt_nodes = optimized["num_internal_nodes"]

    original_counter = Counter(original_sigs.values())
    optimized_counter = Counter(optimized_sigs.values())

    exact_matches = 0
    for sig, optimized_count in optimized_counter.items():
        exact_matches += min(optimized_count, original_counter.get(sig, 0))

    old_disappeared = 0
    for sig, original_count in original_counter.items():
        old_disappeared += max(0, original_count - optimized_counter.get(sig, 0))

    new_appeared = 0
    for sig, optimized_count in optimized_counter.items():
        new_appeared += max(0, optimized_count - original_counter.get(sig, 0))

    # Per-optimized-node best scores across all original nodes
    best_support_scores: list[float] = []
    best_sim_scores: list[float] = []
    best_combined_scores: list[float] = []
    original_nodes_list = list(original_sigs.keys())
    total_patterns = min(original["total_patterns"], optimized["total_patterns"])

    for opt_node, opt_sig in optimized_sigs.items():
        opt_support = set(optimized["support"][opt_node])
        opt_level = optimized["level"][opt_node]

        best_sup = best_sim = best_comb = 0.0
        for orig_node, orig_sig in original_sigs.items():
            orig_support = set(original["support"][orig_node])
            orig_level = original["level"][orig_node]

            sim = bit_similarity(opt_sig, orig_sig, total_patterns)
            sup = jaccard(opt_support, orig_support)
            dep = depth_similarity(opt_level, orig_level)
            comb = combined_candidate_score(sim, sup, dep)

            if sup > best_sup:
                best_sup = sup
            if sim > best_sim:
                best_sim = sim
            if comb > best_comb:
                best_comb = comb

        best_support_scores.append(best_sup)
        best_sim_scores.append(best_sim)
        best_combined_scores.append(best_comb)

    def _avg(lst):
        return statistics.mean(lst) if lst else 0.0

    return {
        # ---- original columns (unchanged) ----
        "original_nodes": orig_nodes,
        "optimized_nodes": opt_nodes,
        "original_levels": original["max_level"],
        "optimized_levels": optimized["max_level"],
        "exact_internal_matches": exact_matches,
        "old_signatures_disappeared": old_disappeared,
        "new_signatures_appeared": new_appeared,
        "avg_best_support_overlap": _avg(best_support_scores),
        "simulation_mode": "exact" if original["exact_simulation"] else "random",
        # ---- new research metrics ----
        "exact_match_rate": safe_rate(exact_matches, opt_nodes),
        "node_reduction_rate": safe_rate(orig_nodes - opt_nodes, orig_nodes),
        "level_reduction_rate": safe_rate(
            original["max_level"] - optimized["max_level"], original["max_level"]
        ),
        "avg_best_simulation_similarity": _avg(best_sim_scores),
        "avg_best_combined_score": _avg(best_combined_scores),
        "support_overlap_min": min(best_support_scores) if best_support_scores else 0.0,
        "support_overlap_median": (
            statistics.median(best_support_scores) if best_support_scores else 0.0
        ),
        "support_overlap_max": max(best_support_scores) if best_support_scores else 0.0,
    }


def rank_candidates(original, optimized, benchmark, optimization):
    """
    For every optimized internal node, rank original internal nodes as possible matches.

    Only the top-TOP_K candidates per optimized node are kept.  The downstream
    sat_refinement_placeholder.py then narrows this further to rank-1 entries
    above the SAT threshold — those are the pairs that get submitted to ABC CEC.

    New columns added (beyond the original set):
      optimized_fingerprint / original_fingerprint  — deterministic SHA-256 hex IDs
      optimized_support_size / original_support_size — |support| cardinalities
      optimized_fanin_count  / original_fanin_count  — direct fanin counts
    """

    rows = []
    total_patterns = min(original["total_patterns"], optimized["total_patterns"])

    opt_fingerprints = optimized.get("fingerprints", {})
    orig_fingerprints = original.get("fingerprints", {})
    opt_fanin = optimized.get("fanin_count", {})
    orig_fanin = original.get("fanin_count", {})

    for opt_node, opt_sig in optimized["signatures"].items():
        candidates = []

        opt_support_set = set(optimized["support"][opt_node])
        opt_level = optimized["level"][opt_node]
        opt_fp = opt_fingerprints.get(opt_node, "")
        opt_fanin_n = opt_fanin.get(opt_node, 0)

        for orig_node, orig_sig in original["signatures"].items():
            orig_support_set = set(original["support"][orig_node])
            orig_level = original["level"][orig_node]

            sim_score = bit_similarity(opt_sig, orig_sig, total_patterns)
            support_score = jaccard(opt_support_set, orig_support_set)
            depth_score = depth_similarity(opt_level, orig_level)
            final_score = combined_candidate_score(sim_score, support_score, depth_score)

            # A candidate is an exact signature match when both nodes produce
            # identical outputs on every simulated input pattern.  For circuits
            # in exact-simulation mode (all 2^n inputs) this is a formal proof
            # of Boolean equivalence.  For random-mode circuits it is a strong
            # indicator but not a proof.
            is_exact = 1 if sim_score == 1.0 else 0
            match_category = "exact_anchor" if is_exact else "non_exact_candidate"

            candidates.append({
                "benchmark": benchmark,
                "optimization": optimization,
                "optimized_node": opt_node,
                "original_candidate": orig_node,
                "combined_score": final_score,
                "simulation_similarity": sim_score,
                "support_overlap": support_score,
                "depth_similarity": depth_score,
                "optimized_level": opt_level,
                "original_level": orig_level,
                # exact-match metadata (added to address methodological clarity)
                "is_exact_signature_match": is_exact,
                "match_category": match_category,
                # new fingerprint / structural columns
                "optimized_fingerprint": opt_fp,
                "original_fingerprint": orig_fingerprints.get(orig_node, ""),
                "optimized_support_size": len(opt_support_set),
                "original_support_size": len(orig_support_set),
                "optimized_fanin_count": opt_fanin_n,
                "original_fanin_count": orig_fanin.get(orig_node, 0),
            })

        candidates.sort(key=lambda row: row["combined_score"], reverse=True)

        for rank, row in enumerate(candidates[:TOP_K], start=1):
            row["rank"] = rank
            rows.append(row)

    return rows


