#!/usr/bin/env python3

"""
Small BLIF analysis script for the initial correspondence experiments.

This is intentionally not a full synthesis/correspondence framework yet.
The goal is only to answer a first research question:

    After one optimization, how many internal nodes can still be matched exactly,
    and when exact matching fails, do we still see similar supports/regions?

The script reads files generated by run_abc_variants.sh from the variants/ folder.
It writes two CSV files:

    results/summary_metrics.csv
    results/top_candidates.csv

I kept the implementation direct and readable, because at this stage I care more
about understanding the behavior than about having a very optimized tool.
"""

import csv
import glob
import os
import random
from collections import Counter, defaultdict
from dataclasses import dataclass


# For small circuits, the script enumerates all input assignments exactly.
# For larger circuits, it switches to random simulation, otherwise 2^n becomes too large.
MAX_EXACT_INPUTS = 12
RANDOM_PATTERNS = 4096
RANDOM_SEED = 7
TOP_K = 5


@dataclass
class BlifNode:
    output: str
    inputs: list[str]
    cover: list[str]


@dataclass
class BlifNetwork:
    inputs: list[str]
    outputs: list[str]
    nodes: list[BlifNode]


# -----------------------------------------------------------------------------
# Basic BLIF parsing
# -----------------------------------------------------------------------------


def read_logical_lines(path):
    """Read BLIF lines and join simple continuation lines ending in '\\'."""

    logical_lines = []
    current = ""

    with open(path, "r", encoding="utf-8") as f:
        for raw_line in f:
            line = raw_line.strip()

            if not line:
                if current:
                    logical_lines.append(current)
                    current = ""
                continue

            if line.endswith("\\"):
                current += line[:-1].strip() + " "
            else:
                current += line
                logical_lines.append(current.strip())
                current = ""

    if current:
        logical_lines.append(current.strip())

    return logical_lines


def parse_blif(path):
    """Parse only the BLIF subset needed for these small experiments."""

    lines = read_logical_lines(path)
    net = BlifNetwork(inputs=[], outputs=[], nodes=[])

    i = 0
    while i < len(lines):
        line = lines[i]

        if not line or line.startswith("#"):
            i += 1
            continue

        parts = line.split()
        directive = parts[0]

        if directive == ".inputs":
            net.inputs.extend(parts[1:])
            i += 1

        elif directive == ".outputs":
            net.outputs.extend(parts[1:])
            i += 1

        elif directive == ".names":
            # .names fanin1 fanin2 ... output
            # Following lines are the cover rows.
            names = parts[1:]
            node_inputs = names[:-1]
            node_output = names[-1]
            cover = []

            i += 1
            while i < len(lines):
                next_line = lines[i]

                if not next_line or next_line.startswith("#"):
                    i += 1
                    continue

                if next_line.startswith("."):
                    break

                cover.append(next_line)
                i += 1

            net.nodes.append(BlifNode(node_output, node_inputs, cover))

        else:
            # I ignore directives such as .model and .end here.
            # If later the benchmarks become more complex, this parser should be extended.
            i += 1

    return net


# -----------------------------------------------------------------------------
# Simulation
# -----------------------------------------------------------------------------


def make_input_patterns(inputs):
    """
    Build bit-vector simulation patterns.

    For very small circuits, this is exact: every possible input assignment is used.
    For bigger circuits, I use deterministic random simulation, which is enough for
    an approximate first matching score.
    """

    random.seed(RANDOM_SEED)
    values = {}

    if len(inputs) <= MAX_EXACT_INPUTS:
        total_patterns = 1 << len(inputs)
        exact = True

        for input_index, name in enumerate(inputs):
            bits = 0
            for assignment in range(total_patterns):
                bit = (assignment >> input_index) & 1
                if bit:
                    bits |= 1 << assignment
            values[name] = bits

    else:
        total_patterns = RANDOM_PATTERNS
        exact = False

        for name in inputs:
            bits = 0
            for pattern_index in range(total_patterns):
                if random.getrandbits(1):
                    bits |= 1 << pattern_index
            values[name] = bits

    mask = (1 << total_patterns) - 1
    return values, mask, total_patterns, exact


def eval_cover(node, values, mask):
    """Evaluate one .names node as a sum of product cubes."""

    # Constant node case.
    if len(node.inputs) == 0:
        for row in node.cover:
            if row.strip() == "1":
                return mask
        return 0

    result = 0

    for row in node.cover:
        parts = row.split()

        # BLIF rows can be written as either "pattern 1" or just "1" for constants.
        pattern = parts[0]
        out_value = parts[1] if len(parts) > 1 else "1"

        if out_value != "1":
            continue

        cube_bits = mask

        for bit_char, input_name in zip(pattern, node.inputs):
            if bit_char == "1":
                cube_bits &= values[input_name]
            elif bit_char == "0":
                cube_bits &= (~values[input_name]) & mask
            elif bit_char == "-":
                pass
            else:
                raise ValueError(f"Unsupported BLIF cover character: {bit_char}")

        result |= cube_bits

    return result


def analyze_network(path):
    """Compute signatures, supports, and levels for one BLIF network."""

    net = parse_blif(path)
    values, mask, total_patterns, exact = make_input_patterns(net.inputs)

    support = {}
    level = {}

    for primary_input in net.inputs:
        support[primary_input] = frozenset([primary_input])
        level[primary_input] = 0

    internal_signatures = {}

    for node in net.nodes:
        missing_fanins = [fanin for fanin in node.inputs if fanin not in values]
        if missing_fanins:
            raise ValueError(f"In {path}, missing fanin values: {missing_fanins}")

        values[node.output] = eval_cover(node, values, mask)

        node_support = set()
        node_level = 0

        for fanin in node.inputs:
            node_support |= set(support.get(fanin, frozenset()))
            node_level = max(node_level, level.get(fanin, 0) + 1)

        support[node.output] = frozenset(node_support)
        level[node.output] = node_level

        # Focus on internal nodes only.
        # Primary outputs are preserved by construction in most ABC optimizations,
        # so comparing them would inflate the match rate without testing anything useful.
        # The interesting question is whether the intermediate logic is recoverable.
        if node.output not in net.outputs:
            internal_signatures[node.output] = values[node.output]

    max_level = 0
    for output in net.outputs:
        max_level = max(max_level, level.get(output, 0))

    return {
        "path": path,
        "inputs": net.inputs,
        "outputs": net.outputs,
        "num_internal_nodes": len(internal_signatures),
        "max_level": max_level,
        "signatures": internal_signatures,
        "support": support,
        "level": level,
        "total_patterns": total_patterns,
        "exact_simulation": exact,
        "mask": mask,
    }


# -----------------------------------------------------------------------------
# Matching metrics
# -----------------------------------------------------------------------------


def jaccard(a, b):
    """Jaccard overlap between two supports."""

    if not a and not b:
        return 1.0
    if not a or not b:
        return 0.0
    return len(a & b) / len(a | b)


def bit_similarity(sig_a, sig_b, total_patterns):
    """
    Similarity between two simulated Boolean signatures.

    1.0 means they agree on all simulated patterns.
    0.5 means random-like agreement.
    0.0 means complete disagreement.
    """

    diff = sig_a ^ sig_b
    mismatches = diff.bit_count()
    return 1.0 - (mismatches / total_patterns)


def depth_similarity(depth_a, depth_b):
    """Small helper so nodes at similar depths get a slightly better score."""

    diff = abs(depth_a - depth_b)
    return 1.0 / (1.0 + diff)


def combined_candidate_score(sim_score, support_score, depth_score):
    """
    Weighted combination of simulation similarity, support overlap, and depth proximity.

    Weights chosen empirically for Experiment 2:
      0.55 simulation  — direct Boolean agreement is the strongest signal
      0.35 support     — shared input cone is the best structural backup when
                         simulation is inconclusive (e.g. under random patterns)
      0.10 depth       — depth alone is a weak signal but breaks ties naturally

    This is intentionally a simple, transparent baseline, not the final result.
    """

    return 0.55 * sim_score + 0.35 * support_score + 0.10 * depth_score


def compare_networks(original, optimized):
    """
    Compute summary metrics for original vs optimized network.

    The support-overlap average is the main number to watch: if optimization
    restructures logic but preserves the same cone of inputs, the Jaccard
    scores will stay high even when bit signatures diverge.  A low support
    overlap means the optimization genuinely changed which inputs a node depends
    on — that is a harder correspondence to recover later with SAT.
    """

    original_sigs = original["signatures"]
    optimized_sigs = optimized["signatures"]

    original_counter = Counter(original_sigs.values())
    optimized_counter = Counter(optimized_sigs.values())

    exact_matches = 0
    for sig, optimized_count in optimized_counter.items():
        exact_matches += min(optimized_count, original_counter.get(sig, 0))

    old_disappeared = 0
    for sig, original_count in original_counter.items():
        old_disappeared += max(0, original_count - optimized_counter.get(sig, 0))

    new_appeared = 0
    for sig, optimized_count in optimized_counter.items():
        new_appeared += max(0, optimized_count - original_counter.get(sig, 0))

    best_support_scores = []
    original_nodes = list(original_sigs.keys())

    for optimized_node in optimized_sigs.keys():
        optimized_support = set(optimized["support"][optimized_node])

        best = 0.0
        for original_node in original_nodes:
            original_support = set(original["support"][original_node])
            best = max(best, jaccard(optimized_support, original_support))

        best_support_scores.append(best)

    avg_best_support = (
        sum(best_support_scores) / len(best_support_scores)
        if best_support_scores else 0.0
    )

    return {
        "original_nodes": original["num_internal_nodes"],
        "optimized_nodes": optimized["num_internal_nodes"],
        "original_levels": original["max_level"],
        "optimized_levels": optimized["max_level"],
        "exact_internal_matches": exact_matches,
        "old_signatures_disappeared": old_disappeared,
        "new_signatures_appeared": new_appeared,
        "avg_best_support_overlap": avg_best_support,
        "simulation_mode": "exact" if original["exact_simulation"] else "random",
    }


def rank_candidates(original, optimized, benchmark, optimization):
    """
    For every optimized internal node, rank original internal nodes as possible matches.

    Only the top-TOP_K candidates per optimized node are kept.  The downstream
    sat_refinement_placeholder.py then narrows this further to rank-1 entries
    above the SAT threshold — those are the pairs that get submitted to ABC CEC.
    """

    rows = []
    total_patterns = min(original["total_patterns"], optimized["total_patterns"])

    for opt_node, opt_sig in optimized["signatures"].items():
        candidates = []

        for orig_node, orig_sig in original["signatures"].items():
            sim_score = bit_similarity(opt_sig, orig_sig, total_patterns)
            support_score = jaccard(
                set(optimized["support"][opt_node]),
                set(original["support"][orig_node]),
            )
            depth_score = depth_similarity(
                optimized["level"][opt_node],
                original["level"][orig_node],
            )
            final_score = combined_candidate_score(sim_score, support_score, depth_score)

            candidates.append({
                "benchmark": benchmark,
                "optimization": optimization,
                "optimized_node": opt_node,
                "original_candidate": orig_node,
                "combined_score": final_score,
                "simulation_similarity": sim_score,
                "support_overlap": support_score,
                "depth_similarity": depth_score,
                "optimized_level": optimized["level"][opt_node],
                "original_level": original["level"][orig_node],
            })

        candidates.sort(key=lambda row: row["combined_score"], reverse=True)

        for rank, row in enumerate(candidates[:TOP_K], start=1):
            row["rank"] = rank
            rows.append(row)

    return rows


# -----------------------------------------------------------------------------
# Folder handling and reporting
# -----------------------------------------------------------------------------


def group_variant_files():
    """Group files like toy_balance.blif by benchmark and optimization name."""

    known_opts = ["resyn2_like", "original", "balance", "rewrite", "refactor", "resub"]
    grouped = defaultdict(dict)

    for path in sorted(glob.glob("variants/*.blif")):
        filename = os.path.basename(path).replace(".blif", "")

        matched = False
        for opt in known_opts:
            suffix = "_" + opt
            if filename.endswith(suffix):
                benchmark = filename[:-len(suffix)]
                grouped[benchmark][opt] = path
                matched = True
                break

        if not matched:
            print(f"Warning: could not classify variant file {path}")

    return grouped


def write_csv(path, rows, fieldnames):
    os.makedirs(os.path.dirname(path), exist_ok=True)

    with open(path, "w", newline="", encoding="utf-8") as f:
        writer = csv.DictWriter(f, fieldnames=fieldnames)
        writer.writeheader()
        writer.writerows(rows)


def main():
    grouped = group_variant_files()

    if not grouped:
        print("No variants found. Run ./run_abc_variants.sh first.")
        return

    summary_rows = []
    candidate_rows = []

    for benchmark, files in grouped.items():
        if "original" not in files:
            print(f"Skipping {benchmark}: no original variant found.")
            continue

        print()
        print("=" * 80)
        print(f"Benchmark: {benchmark}")
        print("=" * 80)

        original = analyze_network(files["original"])

        for optimization, path in sorted(files.items()):
            if optimization == "original":
                continue

            optimized = analyze_network(path)
            metrics = compare_networks(original, optimized)

            row = {"benchmark": benchmark, "optimization": optimization}
            row.update(metrics)
            summary_rows.append(row)

            candidate_rows.extend(rank_candidates(original, optimized, benchmark, optimization))

            print()
            print(f"Optimization: {optimization}")
            print("-" * 40)
            for key, value in metrics.items():
                if isinstance(value, float):
                    print(f"{key}: {value:.3f}")
                else:
                    print(f"{key}: {value}")

    summary_fields = [
        "benchmark",
        "optimization",
        "original_nodes",
        "optimized_nodes",
        "original_levels",
        "optimized_levels",
        "exact_internal_matches",
        "old_signatures_disappeared",
        "new_signatures_appeared",
        "avg_best_support_overlap",
        "simulation_mode",
    ]

    candidate_fields = [
        "benchmark",
        "optimization",
        "optimized_node",
        "rank",
        "original_candidate",
        "combined_score",
        "simulation_similarity",
        "support_overlap",
        "depth_similarity",
        "optimized_level",
        "original_level",
    ]

    write_csv("results/summary_metrics.csv", summary_rows, summary_fields)
    write_csv("results/top_candidates.csv", candidate_rows, candidate_fields)

    print()
    print("Saved results:")
    print("  results/summary_metrics.csv")
    print("  results/top_candidates.csv")


if __name__ == "__main__":
    main()
