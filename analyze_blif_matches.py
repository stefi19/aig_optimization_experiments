#!/usr/bin/env python3

"""
Small BLIF analysis script for the initial correspondence experiments.

This is intentionally not a full synthesis/correspondence framework yet.
The goal is only to answer a first research question:

    After one optimization, how many internal nodes can still be matched exactly,
    and when exact matching fails, do we still see similar supports/regions?

The script reads files generated by run_abc_variants.sh from the variants/ folder.
It writes three CSV files:

    results/summary_metrics.csv    — per benchmark/optimization summary
    results/top_candidates.csv     — top-K candidate pairs per optimized node
    results/node_fingerprints.csv  — deterministic fingerprint for every internal node

I kept the implementation direct and readable, because at this stage I care more
about understanding the behavior than about having a very optimized tool.
"""

import csv
import glob
import hashlib
import os
import random
import statistics
from collections import Counter, defaultdict
from dataclasses import dataclass


# For small circuits, the script enumerates all input assignments exactly.
# For larger circuits, it switches to random simulation, otherwise 2^n becomes too large.
MAX_EXACT_INPUTS = 12
RANDOM_PATTERNS = 4096
RANDOM_SEED = 7
TOP_K = 5


@dataclass
class BlifNode:
    output: str
    inputs: list[str]
    cover: list[str]


@dataclass
class BlifNetwork:
    inputs: list[str]
    outputs: list[str]
    nodes: list[BlifNode]


# -----------------------------------------------------------------------------
# Basic BLIF parsing
# -----------------------------------------------------------------------------


def read_logical_lines(path):
    """Read BLIF lines and join simple continuation lines ending in '\\'."""

    logical_lines = []
    current = ""

    with open(path, "r", encoding="utf-8") as f:
        for raw_line in f:
            line = raw_line.strip()

            if not line:
                if current:
                    logical_lines.append(current)
                    current = ""
                continue

            if line.endswith("\\"):
                current += line[:-1].strip() + " "
            else:
                current += line
                logical_lines.append(current.strip())
                current = ""

    if current:
        logical_lines.append(current.strip())

    return logical_lines


def parse_blif(path):
    """Parse only the BLIF subset needed for these small experiments."""

    lines = read_logical_lines(path)
    net = BlifNetwork(inputs=[], outputs=[], nodes=[])

    i = 0
    while i < len(lines):
        line = lines[i]

        if not line or line.startswith("#"):
            i += 1
            continue

        parts = line.split()
        directive = parts[0]

        if directive == ".inputs":
            net.inputs.extend(parts[1:])
            i += 1

        elif directive == ".outputs":
            net.outputs.extend(parts[1:])
            i += 1

        elif directive == ".names":
            # .names fanin1 fanin2 ... output
            # Following lines are the cover rows.
            names = parts[1:]
            node_inputs = names[:-1]
            node_output = names[-1]
            cover = []

            i += 1
            while i < len(lines):
                next_line = lines[i]

                if not next_line or next_line.startswith("#"):
                    i += 1
                    continue

                if next_line.startswith("."):
                    break

                cover.append(next_line)
                i += 1

            net.nodes.append(BlifNode(node_output, node_inputs, cover))

        else:
            # I ignore directives such as .model and .end here.
            # If later the benchmarks become more complex, this parser should be extended.
            i += 1

    return net


# -----------------------------------------------------------------------------
# Simulation
# -----------------------------------------------------------------------------


def make_input_patterns(inputs):
    """
    Build bit-vector simulation patterns.

    For very small circuits, this is exact: every possible input assignment is used.
    For bigger circuits, I use deterministic random simulation, which is enough for
    an approximate first matching score.
    """

    random.seed(RANDOM_SEED)
    values = {}

    if len(inputs) <= MAX_EXACT_INPUTS:
        total_patterns = 1 << len(inputs)
        exact = True

        for input_index, name in enumerate(inputs):
            bits = 0
            for assignment in range(total_patterns):
                bit = (assignment >> input_index) & 1
                if bit:
                    bits |= 1 << assignment
            values[name] = bits

    else:
        total_patterns = RANDOM_PATTERNS
        exact = False

        for name in inputs:
            bits = 0
            for pattern_index in range(total_patterns):
                if random.getrandbits(1):
                    bits |= 1 << pattern_index
            values[name] = bits

    mask = (1 << total_patterns) - 1
    return values, mask, total_patterns, exact


def eval_cover(node, values, mask):
    """Evaluate one .names node as a sum of product cubes."""

    # Constant node case.
    if len(node.inputs) == 0:
        for row in node.cover:
            if row.strip() == "1":
                return mask
        return 0

    result = 0

    for row in node.cover:
        parts = row.split()

        # BLIF rows can be written as either "pattern 1" or just "1" for constants.
        pattern = parts[0]
        out_value = parts[1] if len(parts) > 1 else "1"

        if out_value != "1":
            continue

        cube_bits = mask

        for bit_char, input_name in zip(pattern, node.inputs):
            if bit_char == "1":
                cube_bits &= values[input_name]
            elif bit_char == "0":
                cube_bits &= (~values[input_name]) & mask
            elif bit_char == "-":
                pass
            else:
                raise ValueError(f"Unsupported BLIF cover character: {bit_char}")

        result |= cube_bits

    return result


def analyze_network(path):
    """
    Compute signatures, supports, levels, fanin counts, and fingerprints for one BLIF.

    Returns a dict with all per-node data needed downstream.
    """

    net = parse_blif(path)
    values, mask, total_patterns, exact = make_input_patterns(net.inputs)

    support = {}
    level = {}
    fanin_count = {}   # direct fanin count from the .names statement

    for primary_input in net.inputs:
        support[primary_input] = frozenset([primary_input])
        level[primary_input] = 0
        fanin_count[primary_input] = 0

    internal_signatures = {}

    for node in net.nodes:
        missing_fanins = [fanin for fanin in node.inputs if fanin not in values]
        if missing_fanins:
            raise ValueError(f"In {path}, missing fanin values: {missing_fanins}")

        values[node.output] = eval_cover(node, values, mask)

        node_support = set()
        node_level = 0

        for fanin in node.inputs:
            node_support |= set(support.get(fanin, frozenset()))
            node_level = max(node_level, level.get(fanin, 0) + 1)

        support[node.output] = frozenset(node_support)
        level[node.output] = node_level
        fanin_count[node.output] = len(node.inputs)

        # Focus on internal nodes only.
        # Primary outputs are preserved by construction in most ABC optimizations,
        # so comparing them would inflate the match rate without testing anything useful.
        # The interesting question is whether the intermediate logic is recoverable.
        if node.output not in net.outputs:
            internal_signatures[node.output] = values[node.output]

    max_level = 0
    for output in net.outputs:
        max_level = max(max_level, level.get(output, 0))

    # Build deterministic fingerprints for every internal node.
    # These are used by sat_refinement_abc.py to attempt name-independent
    # recovery when ABC renames a node during optimization.
    fingerprints = {
        name: compute_fingerprint(
            support=support.get(name, frozenset()),
            lv=level.get(name, 0),
            fc=fanin_count.get(name, 0),
            sig=sig,
        )
        for name, sig in internal_signatures.items()
    }

    return {
        "path": path,
        "inputs": net.inputs,
        "outputs": net.outputs,
        "num_inputs": len(net.inputs),
        "num_outputs": len(net.outputs),
        "num_internal_nodes": len(internal_signatures),
        "max_level": max_level,
        "signatures": internal_signatures,
        "support": support,
        "level": level,
        "fanin_count": fanin_count,
        "fingerprints": fingerprints,
        "total_patterns": total_patterns,
        "exact_simulation": exact,
        "mask": mask,
    }



# -----------------------------------------------------------------------------
# Deterministic node fingerprinting
# -----------------------------------------------------------------------------


def compute_fingerprint(support: frozenset, lv: int, fc: int, sig: int) -> str:
    """
    Return a 16-character deterministic hex fingerprint for one internal node.

    The fingerprint encodes:
      - the node's support set (sorted primary-input names)
      - its logic level
      - its direct fanin count
      - a hex prefix of its simulation signature

    We use SHA-256 (not Python's built-in hash) because Python's hash is
    randomised between interpreter invocations, which would make fingerprints
    non-reproducible across runs.

    sat_refinement_abc.py uses this fingerprint to attempt name-independent
    recovery when ABC renames a node during optimization.
    """
    support_str = ",".join(sorted(support))
    sig_hex = format(sig, "x") if sig else "0"
    data = f"{support_str}|{lv}|{fc}|{sig_hex}"
    return hashlib.sha256(data.encode()).hexdigest()[:16]


