def group_variant_files():
    """Group files like toy_balance.blif by benchmark and optimization name."""

    # Longer suffixes must come before shorter ones so that e.g. 'resyn2_like'
    # is matched before 'resyn2', and 'rewrite_z' before 'rewrite'.
    known_opts = [
        "resyn2_like", "compress2rs", "refactor_z", "rewrite_z",
        "resyn2", "resyn", "dc2",
        "original", "balance", "rewrite", "refactor", "resub",
    ]
    grouped = defaultdict(dict)

    for path in sorted(glob.glob("variants/*.blif")):
        filename = os.path.basename(path).replace(".blif", "")

        matched = False
        for opt in known_opts:
            suffix = "_" + opt
            if filename.endswith(suffix):
                benchmark = filename[:-len(suffix)]
                grouped[benchmark][opt] = path
                matched = True
                break

        if not matched:
            print(f"Warning: could not classify variant file {path}")

    return grouped


def write_csv(path, rows, fieldnames):
    os.makedirs(os.path.dirname(path), exist_ok=True)

    with open(path, "w", newline="", encoding="utf-8") as f:
        writer = csv.DictWriter(f, fieldnames=fieldnames)
        writer.writeheader()
        writer.writerows(rows)


def main():
    grouped = group_variant_files()

    if not grouped:
        print("No variants found. Run ./run_abc_variants.sh first.")
        return

    summary_rows = []
    candidate_rows = []
    fingerprint_rows = []

    for benchmark, files in grouped.items():
        if "original" not in files:
            print(f"Skipping {benchmark}: no original variant found.")
            continue

        print()
        print("=" * 80)
        print(f"Benchmark: {benchmark}")
        print("=" * 80)

        original = analyze_network(files["original"])

        # Collect fingerprints for the original variant
        for node_name, fp in original.get("fingerprints", {}).items():
            fingerprint_rows.append({
                "benchmark": benchmark,
                "optimization": "original",
                "node": node_name,
                "fingerprint": fp,
                "level": original["level"].get(node_name, ""),
                "fanin_count": original.get("fanin_count", {}).get(node_name, ""),
                "support_size": len(original["support"].get(node_name, [])),
            })

        for optimization, path in sorted(files.items()):
            if optimization == "original":
                continue

            optimized = analyze_network(path)
            metrics = compare_networks(original, optimized)

            row = {
                "benchmark": benchmark,
                "optimization": optimization,
                "benchmark_family": infer_benchmark_family(benchmark),
                "optimization_group": infer_optimization_group(optimization),
            }
            row.update(metrics)
            summary_rows.append(row)

            candidate_rows.extend(
                rank_candidates(original, optimized, benchmark, optimization)
            )

            # Collect fingerprints for this optimized variant
            for node_name, fp in optimized.get("fingerprints", {}).items():
                fingerprint_rows.append({
                    "benchmark": benchmark,
                    "optimization": optimization,
                    "node": node_name,
                    "fingerprint": fp,
                    "level": optimized["level"].get(node_name, ""),
                    "fanin_count": optimized.get("fanin_count", {}).get(node_name, ""),
                    "support_size": len(optimized["support"].get(node_name, [])),
                })

            print()
            print(f"Optimization: {optimization}")
            print("-" * 40)
            for key, value in metrics.items():
                if isinstance(value, float):
                    print(f"{key}: {value:.3f}")
                else:
                    print(f"{key}: {value}")

    summary_fields = [
        "benchmark",
        "optimization",
        "benchmark_family",
        "optimization_group",
        "original_nodes",
        "optimized_nodes",
        "original_levels",
        "optimized_levels",
        "exact_internal_matches",
        "old_signatures_disappeared",
        "new_signatures_appeared",
        "avg_best_support_overlap",
        "simulation_mode",
        # new research metrics
        "exact_match_rate",
        "node_reduction_rate",
        "level_reduction_rate",
        "avg_best_simulation_similarity",
        "avg_best_combined_score",
        "support_overlap_min",
        "support_overlap_median",
        "support_overlap_max",
    ]

    candidate_fields = [
        "benchmark",
        "optimization",
        "optimized_node",
        "rank",
        "original_candidate",
        "combined_score",
        "simulation_similarity",
        "support_overlap",
        "depth_similarity",
        "optimized_level",
        "original_level",
        # exact-match metadata
        "is_exact_signature_match",
        "match_category",
        # new structural columns
        "optimized_fingerprint",
        "original_fingerprint",
        "optimized_support_size",
        "original_support_size",
        "optimized_fanin_count",
        "original_fanin_count",
    ]

    fingerprint_fields = [
        "benchmark",
        "optimization",
        "node",
        "fingerprint",
        "level",
        "fanin_count",
        "support_size",
    ]

    write_csv("results/summary_metrics.csv", summary_rows, summary_fields)
    write_csv("results/top_candidates.csv", candidate_rows, candidate_fields)
    write_csv("results/node_fingerprints.csv", fingerprint_rows, fingerprint_fields)

    print()
    print("Saved results:")
    print("  results/summary_metrics.csv")
    print("  results/top_candidates.csv")
    print("  results/node_fingerprints.csv")


if __name__ == "__main__":
    main()
