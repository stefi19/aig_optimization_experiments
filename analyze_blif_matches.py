# Safe arithmetic helpers
# -----------------------------------------------------------------------------


def safe_rate(numerator: int | float, denominator: int | float) -> float:
    """Return numerator/denominator, or 0.0 when denominator is zero."""
    if not denominator:
        return 0.0
    return numerator / denominator


